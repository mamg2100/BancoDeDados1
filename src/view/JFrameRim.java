    private void buttonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOKActionPerformed

        lblCadastro.setText("");
        lblCadastro.setForeground(Color.white);
        // foi tornado global para não ter que declarar novamente

        //rim.setCodRim(Integer.valueOf(txtCetil.getText()));
        requi.setUnidade(cmbUnidade.getSelectedItem().toString());
        requi.setDescricao(txtDescricao.getText());
        requi.setDotacao(txtDO.getText());
        if (rbRim.isSelected()) {
            requi.setTipoRim("RIM");
        } else {
            requi.setTipoRim("RRP");
        }
        requi.setCetil(txtCetil.getText());
        requi.setDataCetil(txtDataCetil.getText());
        //requi.setDataCetilSql(Date.valueOf(txtDataCetilSQL.getText()));
        requi.setValorEstimado(txtValorEstimado.getText());
        requi.setValorReal(txtValorReal.getText());
        requi.setProcesso(txtProcesso.getText());
        requi.setAnoProcesso(Integer.valueOf(txtAnoProcesso.getText()));
        requi.setProcessoContabil(txtProcessoContabil.getText());
        requi.setAnoProcessoContabil(Integer.valueOf(txtAnoProcessoContabil.getText()));
        requi.setContabilidade(lblContabilidade.getText());
        requi.setOrdenadorass(lblOrdenadorAss.getText());
        requi.setCompraprim(lblComprasPrim.getText());
        requi.setOrdenadorEmpenho(lblOrdenadorEmpenho.getText());
        requi.setComprasSeg(lblComprasSeg.getText());
        requi.setDipe(lblDipe.getText());
        requi.setCadastrante(cmbCadastrante.getName());
        requi.setDataCadastro(txtDataCadastro.getText());
        requi.setObservacao(txtAnotacoes.getText());
        requi.setCodUsuario(Integer.valueOf(lblCodigoUsuario.getText()));
        requi.setCodUnidade(Integer.valueOf(lblCodigoDespesa.getText()));

        // Condição que controla qual método vai ser utilizado dependendo do radio button selecionado.
        if (rbNovo.isSelected()) {
            //JOptionPane.showMessageDialog(null, "Chama o método de inserção da RIM");
            try {
                ri.InserirRequisicao(requi);
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(null, "Erro! Motivo/n" + ex);
            }
        } else if (rbExcluir.isSelected()) {
            if (!"".equals(lblCodigo.getText())) {
                requi.setCodRim(Integer.valueOf(lblCodigo.getText()));
                ri.ExcluirRequisicao(requi);
            } else {
                JOptionPane.showMessageDialog(null, "Escolha na tabela o item a ser excluído.");
            }
        } else {
            if (!"".equals(lblCodigo.getText())) {
                requi.setCodRim(Integer.valueOf(lblCodigo.getText()));
                ri.AlterarRequisicao(requi);
            } else {
                JOptionPane.showMessageDialog(null, "Escolha na tabela o item a ser alterado.");
            }
        }
    }//GEN-LAST:event_buttonOKActionPerformed

