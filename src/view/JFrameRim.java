    private void populaCxCombinacaoCadastrante() {

        try {
            cmbCadastrante.removeAllItems();
            conecta.executaSQL("Select * from usuario order by Cod_usuario");
            while (conecta.rs.next()) {
                cmbCadastrante.addItem(conecta.rs.getString("Login_usuario"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível popular o item.");
        }
    }

    private void populaCxCombinacaoUnidade() {

        try {
            cmbUnidade.removeAllItems();
            conecta.executaSQL("Select * from unidade order by Cod_unidade");

            while (conecta.rs.next()) {
                cmbUnidade.addItem(conecta.rs.getString("Nome_unidade"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível popular o item.");
        }

    }

